class TransmittableFormMixin(ReviewFormMixin):

    def get_trs_layout(self):
        if self.read_only:
            layout = (
                DocumentFieldset(
                    _('Outgoing Transmittal'),
                    'internal_review',
                    OutgoingTrsLayout(),
                    'purpose_of_issue',
                    Field('external_review_due_date', readonly='readonly'),
                ),)
        else:
            layout = (
                DocumentFieldset(
                    _('Outgoing Transmittal'),
                    'internal_review',
                    'purpose_of_issue',
                    Field('external_review_due_date', readonly='readonly'),
                ),)
        return layout
